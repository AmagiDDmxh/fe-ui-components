import React, { FC, HTMLAttributes } from "react";
import { ReactSVG } from "react-svg";

import DocumentAddOutlineSvg from "../files/outline/document-add.svg";

export type DocumentAddOutlineSvgProps = {
  /**
   * 传入组件中的 class 样式名字
   */
  className?: string;
  /**
   * 图标大小
   */
  size?: number;
  /**
   * 图标填充颜色（注：未设置时同时继承自父元素 color 属性）
   */
  color?: string;
} & Pick<HTMLAttributes<HTMLDivElement>, "onClick">;

const defaultProps = {
  size: 24,
} as const;

const DocumentAddOutline: FC<DocumentAddOutlineSvgProps> = ({
  className,
  color,
  size,
  ...rest
}) => {
  return (
    // @ts-expect-error
    <ReactSVG
      className={className}
      wrapper="div"
      src={DocumentAddOutlineSvg}
      beforeInjection={(svg) => {
        svg.setAttribute("width", `${size}px`);
        svg.setAttribute("height", `${size}px`);
        if (color) {
          svg.setAttribute("color", color);
        }
      }}
      {...rest}
    />
  );
};

DocumentAddOutline.defaultProps = defaultProps;

export default DocumentAddOutline;
